//Let is basically a newer version of javascript and even in typescript also we will
//be using it in place of var....
//Boolean
let mybool:boolean = true;
console.log(mybool);
//number type
let mynum:number = 1234;
console.log(mynum);
//string type
let myname:string ='Y-shu';
console.log(myname);
//array
let myarr:number[];
myarr=[1,2,3,4,5];
console.log(myarr);
//tuple
let x:[string,number,string];
x=['Y-shu',1234,'Mickey'];
console.log(x);
//enum
enum myflower {Rose=9,Lilly=7,Lotus=4}
let f:myflower=myflower.Lilly;
console.log(f);
//enum2
enum mymickey {kittu=12,krish=6,munnu=9};
let m:mymickey=mymickey.munnu;
console.log(m);
//any
let y:any=5;
y='Hello.....';
console.log(y);
